public class Favourite_Blogs {
    private int id, blogId, userId;

    public Favourite_Blogs() {
    }

    public Favourite_Blogs(int id, int blogId, int userId) {
        this.id = id;
        this.blogId = blogId;
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBlogId() {
        return blogId;
    }

    public void setBlogId(int blogId) {
        this.blogId = blogId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
    
}
